const pa: Record<string, string> = {};

const TRANSLATIONS: Record<Language, Record<string, string>> = { en, hi, bn, te, mr, ta, gu, kn, ml, pa };

export function useTranslation() {
  const language = useChatStore((s) => s.language);
  const strings: Record<string, string> = TRANSLATIONS[language] ?? en;

  const t = (key: string, fallback?: string): string => {
    return strings[key] ?? (en as Record<string, string>)[key] ?? fallback ?? key;
  };

  return { t, language };
}
